use serde::{Deserialize, Serialize};

/**
Настройки стика: назначенные коды направлений и мёртвая зона

Содержит конфигурацию аналогового стика, включая назначенные
клавиши для каждого направления и параметры мертвой зоны.
*/
#[derive(Default, Debug, Clone, Serialize, Deserialize, PartialEq)]
pub struct Stick {
  /**
  Коды клавиш для направлений стика: [Вверх, Вправо, Вниз, Влево]

  Каждый элемент массива содержит ASCII-код клавиши, которая будет
  нажата при отклонении стика в соответствующем направлении.
  */
  pub word: [u8; 4],

  /**
  Внутренняя (виртуальная) мёртвая зона в процентах от внешней зоны

  Определяет минимальное отклонение стика, необходимое для срабатывания.
  Значение от 1 до 100, где 100 означает максимальную чувствительность.
  */
  pub deadzone: u8,
}
