/// Состояние редактируемой кнопки/стика в UI
#[derive(Debug, Clone, Default)]
pub struct KeypadButton {
  /// Идентификатор кнопки/направления (1..=16 для кнопок, 1..=4 для стика)
  pub id: usize,

  /// Список подписей комбинации для отображения
  pub vec_str: Vec<String>,

  /// Сырые коды комбинации
  pub code: Vec<u8>,

  /// Признак, что редактируется стик (а не кнопка)
  pub is_stick: bool,
}
