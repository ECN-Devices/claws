/**
Состояние редактируемой кнопки/стика в пользовательском интерфейсе

Хранит временное состояние во время редактирования комбинации клавиш
для кнопки или направления стика.
*/
#[derive(Debug, Clone, Default)]
pub struct KeypadButton {
  /// Идентификатор кнопки/направления (1..=16 для кнопок, 1..=4 для стика)
  pub id: usize,

  /// Список читаемых подписей комбинации для отображения в UI
  /// Например: ["Ctrl", "C"] для комбинации Ctrl+C
  pub vec_str: Vec<String>,

  /// Сырые байтовые коды комбинации для отправки на устройство
  pub code: Vec<u8>,

  /// Признак, что редактируется стик (true) или обычная кнопка (false)
  pub is_stick: bool,
}
